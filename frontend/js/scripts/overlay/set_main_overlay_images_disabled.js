function set_main_overlay_images_disabled(state = true) {
	set_element_disabled('first_overlay_image', state)
	set_element_disabled('first_overlay_image_text', state)
	set_element_disabled('second_overlay_image', state)
	set_element_disabled('second_overlay_image_text', state)
	set_element_disabled('third_overlay_image', state)
	set_element_disabled('third_overlay_image_text', state)
}